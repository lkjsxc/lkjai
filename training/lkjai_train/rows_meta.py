def meta(
    row_id: str,
    domain: str,
    skill: str,
    source_ref: str,
    *,
    split: str,
    toolset: str = "none",
    language: str = "en",
    license_name: str = "project-local",
    safety_scope: str = "workspace-safe",
) -> dict:
    return meta_base(
        row_id,
        domain,
        skill,
        source_ref,
        split,
        toolset,
        language,
        license_name,
        safety_scope,
        "repo-derived",
        "repo-derived",
        "none",
    )


